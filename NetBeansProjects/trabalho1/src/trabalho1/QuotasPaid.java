    public void updateTable(SociUM member) {
        
        DefaultTableModel tableModel = (DefaultTableModel) memberContainerTable.getModel();
        tableModel.setRowCount(0); // Remover todas as entradas da table
        
        for(Quota q: member.getQuotas()) {
            String[] data = new String[2];
            data[0] = new SimpleDateFormat("MM-yyyy").format(q.getDate());
            data[1] = q.getValue().toString();
            tableModel.addRow(data);
        }
        tableModel.fireTableDataChanged();
    }

